package model;

public class Student {
	private int enrollment_id;
	private String first_name;
	private String last_name;
	private String gender;
	private String dob;
	private String state;
	private String course;
	private String marks;
	private String doa;
	private String city;
	private long mobile;
	private int ssc_percentage;
	private int hsc_percentage;
	private String category;
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public long getMobile() {
		return mobile;
	}
	public void setMobile(long mobile) {
		this.mobile = mobile;
	}
	public int getSsc_percentage() {
		return ssc_percentage;
	}
	public void setSsc_percentage(int ssc_percentage) {
		this.ssc_percentage = ssc_percentage;
	}
	public int getHsc_percentage() {
		return hsc_percentage;
	}
	public void setHsc_percentage(int hsc_percentage) {
		this.hsc_percentage = hsc_percentage;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getEnrollment_id() {
		return enrollment_id;
	}
	public void setEnrollment_id(int enrollment_id) {
		this.enrollment_id = enrollment_id;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getMarks() {
		return marks;
	}
	public void setMarks(String marks) {
		this.marks = marks;
	}
	public String getDoa() {
		return doa;
	}
	public void setDoa(String doa) {
		this.doa = doa;
	}
	
	
}
